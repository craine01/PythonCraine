#loop
#Let's say we have a list of numbers and we want to print each number in the list. We can use a loop to do this.
numbers = [1, 2, 3, 4, 5]
for number in numbers:
    print(number)
#In this example, we use a for loop to iterate through each number in the list and print it. The variable 'number' takes on the value of each element in the list during each iteration of the loop.